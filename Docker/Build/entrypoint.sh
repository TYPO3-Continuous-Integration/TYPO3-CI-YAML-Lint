#!/bin/sh -l

### Function Header ###########################################################
Header() {
  ##########
  # Prints #
  ##########
  echo "---------------------------------------------"
  echo "-- GitHub Actions TYPO3 YAML Linter ---"
  echo "---------------------------------------------"
  echo "TYPO3 YAML Linter source code can be found at:"
