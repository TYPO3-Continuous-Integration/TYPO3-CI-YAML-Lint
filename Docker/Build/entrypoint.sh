### Function Lint #############################################################
Lint() {
  ##########
  # Prints #
  ##########
  echo "---------------------------------------------"
  echo "-- Running lint... ---"
  echo "---------------------------------------------"
  find . ! -path '*.Build/*' ! -path '*Resources/Private/node_modules/*' -name '*.yml' | xargs yaml-lint
}

#### Function Footer ###########################################################
Footer() {
